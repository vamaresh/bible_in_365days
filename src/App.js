    while (currentChapter <= endChapter && bookIndex < BIBLE_BOOKS.length) {
      const book = translatedBooks[bookIndex];
      if (chapterInBook < BIBLE_BOOKS[bookIndex].chapters) {
        chapters.push({
          book: book.name,
          chapter: chapterInBook + 1
        });
        chapterInBook++;
        currentChapter++;
      } else {
        bookIndex++;
        chapterInBook = 0;
      }
    }
    
    return chapters;
